def print_items(n):
    for i in range(n):
        print(i)

print_items(10)

# n = input size (number of nodes, items, etc.)

# Big-O describes how operations grow as n increases

# If growth is proportional → straight line → O(n)

# If growth is squared → curved upward → O(n²)

# If it performs log n steps → O(log n)